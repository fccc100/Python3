# 字符串
str = '0123456789'

# 输出整个字符串
print(str)

# 输出单个字符
print(str[0])  # 0
print(str[1])  # 1

# 输出索引第2个至第4个字符 左闭右开区间
print(str[2:5]) # 234

# 输出第2个至倒数第二个字符
print(str[2:-1]) #2345678

# 从索引2开始到索引6,每隔两个输出一次
print(str[2:6:2]) #24

# 输出两次
print(str * 2)

str1 = '01234567890123456789'
print(str * 2 == str1) # True